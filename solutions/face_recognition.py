# Processing
def face_recognition(face1, face2):
    countDifferences = 0
    if face1 != face2:
        for i in range(0, 64):
            if face1[i] != face2[i]:
                countDifferences += 1
    return "{}\n{}".format(countDifferences, countDifferences < 5)


